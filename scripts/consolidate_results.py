#!/usr/bin/env python3
"""Consolidate per-dataset OptIForest summaries into one table + LaTeX + plots.

Reads every ``partial/<version>/<dataset>/summary.csv`` produced by
``run_optiforest_study.py`` and writes consolidated artifacts under an output
directory. Optionally merges the paper's reported OptIForest numbers (from
``paper_table1_optiforest.csv``) to build a reproduction-vs-paper comparison.

Nothing is overwritten outside the chosen ``--out-dir``.
"""

from __future__ import annotations

import argparse
import csv
from pathlib import Path

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt  # noqa: E402

# Datasets reproduced from the paper vs. new datasets added for Requirement 2.
NEW_DATASETS = {"annthyroid", "fraud"}


def read_summaries(partial_dir: Path) -> list[dict[str, str]]:
    rows: list[dict[str, str]] = []
    for summary_path in sorted(partial_dir.glob("*/summary.csv")):
        with summary_path.open(newline="") as handle:
            reader = csv.DictReader(handle)
            for row in reader:
                rows.append(row)
    if not rows:
        raise SystemExit(f"No summaries found under {partial_dir}")
    return rows


def read_paper_reference(path: Path) -> dict[str, dict[str, str]]:
    if not path.exists():
        return {}
    ref: dict[str, dict[str, str]] = {}
    with path.open(newline="") as handle:
        lines = [ln for ln in handle if not ln.lstrip().startswith("#")]
    reader = csv.DictReader(lines)
    for row in reader:
        ref[row["dataset"].strip().lower()] = row
    return ref


def write_consolidated_csv(rows: list[dict[str, str]], out_path: Path) -> None:
    fields = [
        "dataset", "source", "n_samples", "n_features", "anomaly_rate_pct",
        "threshold", "completed_runs", "mean_auc_roc", "std_auc_roc",
        "mean_auc_pr", "std_auc_pr", "mean_fit_seconds", "mean_predict_seconds",
    ]
    with out_path.open("w", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields, extrasaction="ignore")
        writer.writeheader()
        for row in rows:
            writer.writerow(row)


def fmt(mean: str, std: str) -> str:
    return f"{100 * float(mean):.1f} $\\pm$ {100 * float(std):.1f}"


def write_reproduced_table(rows: list[dict[str, str]], out_path: Path) -> None:
    lines = [
        "% Auto-generated by consolidate_results.py",
        "\\begin{table}[t]",
        "\\centering",
        "\\caption{Resultados reproduzidos do OptIForest (AUC-ROC e AUC-PR em \\%, "
        "m\\'edia $\\pm$ desvio sobre 15 execu\\c{c}\\~oes). Datasets novos em \\textbf{negrito}.}",
        "\\begin{tabular}{lrrrr}",
        "\\toprule",
        "Dataset & \\#Amostras & Anom.\\% & AUC-ROC & AUC-PR \\\\",
        "\\midrule",
    ]
    for row in rows:
        name = row["dataset"]
        label = f"\\textbf{{{name}}}" if name in NEW_DATASETS else name
        lines.append(
            f"{label} & {int(row['n_samples'])} & {float(row['anomaly_rate_pct']):.2f} "
            f"& {fmt(row['mean_auc_roc'], row['std_auc_roc'])} "
            f"& {fmt(row['mean_auc_pr'], row['std_auc_pr'])} \\\\"
        )
    lines += ["\\bottomrule", "\\end{tabular}", "\\end{table}", ""]
    out_path.write_text("\n".join(lines))


def write_comparison_table(
    rows: list[dict[str, str]], ref: dict[str, dict[str, str]], out_path: Path
) -> None:
    lines = [
        "% Auto-generated by consolidate_results.py",
        "% Paper values transcribed from Table 1 -- VERIFY (see paper_table1_optiforest.csv).",
        "\\begin{table}[t]",
        "\\centering",
        "\\caption{Reprodu\\c{c}\\~ao vs.\\ paper (AUC-ROC \\%). Apenas datasets do paper.}",
        "\\begin{tabular}{lrrr}",
        "\\toprule",
        "Dataset & Paper & Reproduzido & $\\Delta$ \\\\",
        "\\midrule",
    ]
    for row in rows:
        name = row["dataset"]
        if name in NEW_DATASETS or name not in ref:
            continue
        repro = 100 * float(row["mean_auc_roc"])
        paper = float(ref[name]["paper_auc_roc"])
        flag = "$^{*}$" if ref[name].get("verify") == "1" else ""
        lines.append(
            f"{name}{flag} & {paper:.1f} & {repro:.1f} & {repro - paper:+.1f} \\\\"
        )
    lines += [
        "\\bottomrule",
        "\\end{tabular}",
        "\\\\[2pt]\\footnotesize $^{*}$ valor do paper a conferir.",
        "\\end{table}",
        "",
    ]
    out_path.write_text("\n".join(lines))


def plot_bars(rows: list[dict[str, str]], metric: str, title: str, out_path: Path) -> None:
    rows_sorted = sorted(rows, key=lambda r: float(r[f"mean_{metric}"]))
    names = [r["dataset"] for r in rows_sorted]
    means = [100 * float(r[f"mean_{metric}"]) for r in rows_sorted]
    errs = [100 * float(r[f"std_{metric}"]) for r in rows_sorted]
    colors = ["#d1495b" if n in NEW_DATASETS else "#30638e" for n in names]

    fig, ax = plt.subplots(figsize=(10, 6))
    ax.barh(names, means, xerr=errs, color=colors, capsize=2)
    ax.set_xlabel(title)
    ax.set_xlim(0, 100)
    ax.set_title(title + " por dataset (vermelho = datasets novos)")
    fig.tight_layout()
    fig.savefig(out_path, dpi=150)
    plt.close(fig)


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--partial-dir",
        default="results2/results2/partial/v1",
        help="Directory containing <dataset>/summary.csv files.",
    )
    parser.add_argument("--out-dir", default="results2/analysis")
    parser.add_argument(
        "--paper-ref", default="results2/analysis/paper_table1_optiforest.csv"
    )
    args = parser.parse_args()

    partial_dir = Path(args.partial_dir)
    out_dir = Path(args.out_dir)
    plots_dir = out_dir / "plots"
    plots_dir.mkdir(parents=True, exist_ok=True)

    rows = read_summaries(partial_dir)
    rows.sort(key=lambda r: r["dataset"])
    ref = read_paper_reference(Path(args.paper_ref))

    write_consolidated_csv(rows, out_dir / "consolidated_summary.csv")
    write_reproduced_table(rows, out_dir / "reproduced_table.tex")
    if ref:
        write_comparison_table(rows, ref, out_dir / "comparison_table.tex")
    plot_bars(rows, "auc_roc", "AUC-ROC", plots_dir / "auc_roc.png")
    plot_bars(rows, "auc_pr", "AUC-PR", plots_dir / "auc_pr.png")

    print(f"Datasets consolidated: {len(rows)}")
    print(f"  CSV    -> {out_dir / 'consolidated_summary.csv'}")
    print(f"  Tables -> {out_dir / 'reproduced_table.tex'}"
          + (", comparison_table.tex" if ref else ""))
    print(f"  Plots  -> {plots_dir}/auc_roc.png, auc_pr.png")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
